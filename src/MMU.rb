#Memory Management Unit (MMU)
def MMU	
	#Read 8-bit byte from a given address
	def rb(addr)
	end
	
	#Read 16-bit word from a given address
	def rw(addr)
	end
	
	#Write 8-bit byte to a given address
	def wb(addr, val)
	end
	
	#Write 16-bit word to a given address
	def ww(addr, val)
	end
end
